package Array.FirstDimensional;

public class FD_Array {
    public static void main(String[] args) {
        // 배열 : 같은 타입의 변수들을 하나로 묶어 그 값들을 저장하는 방법

        // int형 배열 선언
        int[] students;

        // int 값을 5개 저장할 수 있는 배열 생성
        students = new int[5];

        // 변수 값 대입
        students[0] = 90;
        students[1] = 80;
        students[2] = 70;
        students[3] = 60;
        students[4] = 50;
    }
}
